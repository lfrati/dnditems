def get_item(url):

    response = requests.get(url)
    assert response.status_code == 200
    soup = BeautifulSoup(response.text, "html.parser")

    body = "".join(
        [str(el) for el in list(soup.select_one("#page-content").children)[3:-3]]
    )
    name = soup.select_one(".page-title.page-header span").getText()

    return name, body


